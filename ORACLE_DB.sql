 -- INNER JOIN IN ORACLE
 
SELECT 
     EMPNO, ENAME, JOB, HIREDATE, D.DEPTNO, DNAME 
FROM 
     EMP E, DEPT D
WHERE 
     E.DEPTNO=D.DEPTNO;
     
     
     
  
  
SELECT 
     EMPNO, ENAME, JOB, HIREDATE, D.DEPTNO, DNAME 
FROM 
     EMP E, DEPT D
WHERE 
     E.DEPTNO=D.DEPTNO AND D.DNAME='RESEARCH'
AND
     E.HIREDATE=(SELECT MIN(HIREDATE) FROM EMP E,DEPT D 
     WHERE E.DEPTNO=D.DEPTNO AND D.DNAME='RESEARCH');
     
 
 
 SELECT MIN(HIREDATE), MAX(HIREDATE) FROM EMP;
 
 
 
 SELECT EMPNO,ENAME,JOB,SAL FROM EMP 
 WHERE SAL=(SELECT MAX(SAL) FROM EMP);
 
 
 SELECT 
       EMPNO,ENAME, JOB, SAL,D.DEPTNO,DNAME
 FROM 
       EMP E, DEPT D 
 WHERE 
       E.DEPTNO=D.DEPTNO AND D.DNAME='ACCOUNTING'
 AND 
       SAL=(SELECT MAX(SAL) FROM EMP E, DEPT D 
                  WHERE E.DEPTNO=D.DEPTNO AND D.DNAME='ACCOUNTING');
 
 -- INNER JOIN IN SQL SERVER
SELECT 
       EMPNO,ENAME, JOB, SAL,D.DEPTNO,DNAME
 FROM 
       EMP E INNER JOIN DEPT D
ON
       E.DEPTNO=D.DEPTNO;

 
 -- CROSS JOIN IN ORACLE
   SELECT 
       EMPNO,ENAME, JOB, SAL,D.DEPTNO,DNAME
 FROM 
       EMP E, DEPT D;
 
-- CROSS JOIN IN SQL SERVER
  SELECT 
       EMPNO,ENAME, JOB, SAL,D.DEPTNO,DNAME
 FROM 
       EMP E CROSS JOIN DEPT D;


 -- LEFT OUTER JOIN IN ORACLE
SELECT 
       EMPNO,ENAME, JOB, SAL,D.DEPTNO,DNAME
 FROM 
       EMP E,DEPT D
WHERE
       E.DEPTNO =D.DEPTNO(+);

 -- LEFT OUTER JOIN IN SQL SERVER
SELECT 
       EMPNO,ENAME, JOB, SAL,D.DEPTNO,DNAME
 FROM 
       EMP E LEFT OUTER JOIN DEPT D
ON
       E.DEPTNO=D.DEPTNO;
       

 -- RIGHT OUTER JOIN IN ORACLE
SELECT 
       EMPNO,ENAME, JOB, SAL,D.DEPTNO,DNAME
 FROM 
       EMP E,DEPT D
WHERE
       E.DEPTNO(+) =D.DEPTNO;

 -- RIGHT OUTER JOIN IN SQL SERVER
SELECT 
       EMPNO,ENAME, JOB, SAL,D.DEPTNO,DNAME
 FROM 
       EMP E RIGHT OUTER JOIN DEPT D
ON
       E.DEPTNO=D.DEPTNO;      





 -- FULL OUTER JOIN IN  ORACLE and SQL SERVER
SELECT 
       EMPNO,ENAME, JOB, SAL,D.DEPTNO,DNAME
 FROM 
       EMP E FULL OUTER JOIN DEPT D
ON
       E.DEPTNO=D.DEPTNO;    
       
 
-- SELF JOIN
SELECT 
   E.EMPNO, E.ENAME, E.SAL, M.EMPNO, M.ENAME, M.SAL 
FROM 
   EMP E, EMP M
WHERE
   E.MGR=M.EMPNO
ORDER BY M.EMPNO;



SELECT 
    EMPNO,ENAME,JOB,SAL,S.GRADE
FROM
    EMP E, SALGRADE S
WHERE
    E.SAL BETWEEN S.LOSAL AND S.HISAL;


-- Grade=4 emp.
SELECT 
    EMPNO,ENAME,JOB,SAL,S.GRADE
FROM
    EMP E, SALGRADE S
WHERE
    E.SAL BETWEEN S.LOSAL AND S.HISAL
AND (SAL,S.GRADE) IN (SELECT MAX(SAL),S.GRADE FROM EMP E, SALGRADE S
                                              WHERE E.SAL BETWEEN S.LOSAL AND S.HISAL 
                                              GROUP BY S.GRADE HAVING S.GRADE=4);


-- Grade=2,3 Emps. in Sales Dept
SELECT 
    EMPNO,ENAME,SAL,S.GRADE,D.DEPTNO,DNAME
FROM
    EMP E, DEPT D, SALGRADE S
WHERE
    E.EMPNO AND D.DEPTNO
AND   
    E.SAL BETWEEN S.LOSAL AND S.HISAL
AND
    S.GRADE IN (2,3) AND D.DNAME='SALES';








       
